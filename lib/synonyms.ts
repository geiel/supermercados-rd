function pluralizeWord(word: string) {
  if (word.includes("&")) {
    return [word];
  }
  const variants = new Set<string>();
  variants.add(plural(word) ? plural(word) : word);
  variants.add(pluralizeEs(word) ? pluralizeEs(word) : word);
  return [...variants];
}

for (const [base, syns] of Object.entries(baseMap)) {
  const allSyns = [...syns, base];
  allSyns.forEach((syn) => {
    allSyns.push(...pluralizeWord(syn));
  });

  baseMap[base] = Array.from(new Set<string>([...allSyns]));
}

export const synonyms: Record<string, string[]> = {};
for (const [base, syns] of Object.entries(baseMap)) {
  const group = [base, ...syns];
  for (const w of group) {
    synonyms[w] = group.filter((x) => x !== w);
  }
}
